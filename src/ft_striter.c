#include "../headers/libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int v;

	v = 0;
	if (!s || !f)
		return ;
	while (s[v])
	{
		(*f)(s + v);
		v++;
	}
}
