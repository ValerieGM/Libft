#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	v;
	char	*str;

	v = 0;
	str = (char *)s;
	while (str[v] != c)
	{
		if (str[v] == '\0')
			return (NULL);
		v++;
	}
	return (str + v);
}
